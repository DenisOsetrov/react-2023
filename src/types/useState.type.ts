import {Dispatch, SetStateAction} from "react";

export  type IUseState<T> = Dispatch<SetStateAction<T>>
// Тип Dispatch є частиною API React, який дозволяє змінювати стан компонентів.
// Він приймає значення нового стану і зберігає його у внутрішній пам'яті компонента.
// Тип SetStateAction<T> визначає тип дії, яку слід виконати зі станом.
// Цей тип може приймати будь-яке значення, що може бути використане для зміни стану компонента.